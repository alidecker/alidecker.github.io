// Show/hide project details when clicked
function thisWebFunc() {
    var web = document.getElementById("show-thisWeb");
    var w_bg = document.getElementById("all-thisWeb");
    if (web.style.display === "none") {
        web.style.display = "block";
        w_bg.style.background = "#f7eede";
    } else {
        web.style.display = "none";
        w_bg.style.background = "#F5DEB3";
    }
}
